let expression = "b";
switch (expression) { // expressão
    case "a":
        // se a expressão for "a" execute :
        console.log("a")
        break
    case "b":
        // se a expressão for "b" execute :
        console.log("b")
        break
    case "c":
        // se a expressão for "c" execute :
        console.log("c")
        break
    default :
        // se a expressão não for nenhuma das anteriores execute :
        console.log("defaut");
        break
}